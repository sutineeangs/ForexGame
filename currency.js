class Currency {
    constructor(name, date,time,open,high,low,close,volume){
        this.Name = name;
        this.Date = date;
        this.Time = time;
        this.Open = open;
        this.High = high;
        this.Low = low;
        this.Close = close;
        this.Volume = volume;

    }

}

module.exports = Currency;
